																<p>A main challenge of super-slender buildings is the management of lateral movements due to wind loads, which can cause discomfort for occupants. Various engineering techniques are used to address this issue. One approach to manage the wind loads is to use an open mechanical floor in the middle of a building allows wind to flow through. This can reduce the wind loads and the resulting movements by 10-15%. An example of this technique can be seen in 432 Park Avenue, a 15:1 ratio building that has many open-air mechanical floors. Another approach is to stiffen the structure to resist the loads. This can be done by using outrigger walls to connect the building core to the perimeter columns on two sides to distribute the loads. The structure of 111 West 57th Street uses shear walls on the east and west facades to stiffen the structure while leaving the north and south views unobstructed.</p>

																<p>In term of managing building movements, a tuned mass damper can be used. This is a passive device with a large mass mounted on shock absorbers to slow the movements of the building. Typically they are made of solid mass, but recently liquid damping systems have been used. A liquid damping system or sloshing damper is a tank of water with screens to control the movements of the water, specifically tuned such that when building is moved in one direction, the liquid force goes in the opposite direction to offset the movements. Highcliff is one of the early adopters of such system.</p>

